export function genRandomPokemon() {
    const rnd = Array(5).fill(0).map(() => Math.random());
    const name = pokemonNameList[Math.floor(rnd[0] * pokemonNameList.length)];
    const time = `${Math.floor(rnd[1] * 6.5)}:${Math.floor(rnd[2] * 60).toString().padStart(2, "0")}:${Math.floor(rnd[3] * 60).toString().padStart(2, "0")}.${Math.floor(rnd[4] * 1000).toString().padStart(3, "0")}`;
    return { time, pokemon: name };
}

export function genRandomPokemonList(count: number) {
    return Array(count).fill(0).map(genRandomPokemon);
}
